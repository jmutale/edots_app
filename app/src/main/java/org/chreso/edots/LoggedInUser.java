package org.chreso.edots;

public class LoggedInUser {
    public String first_name;


    public String id;


    public void setId(String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public void setFirst_name(String first_name) {
        this.first_name = first_name;
    }

    public String getFirst_name() {
        return first_name;
    }


}
